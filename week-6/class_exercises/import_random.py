


import random 

# 1. random.randint(a,b) random interger between a and b (inclusive)
print("randint(1,10):", random.randint(1,10))

# 2. random.random() randomfloat between 0.0 and 1.0
print("random():", random.random())

# 3. random.choice(seq) random single element from a sequence
fruits = ["apple", "banana", "cherry"]
print("choices(fruits):", random.choice(fruits))

